

public class Tag {	 

	private String a;
	private String b;
	
	public Tag(String a, String b){
		this.a = a;
		this.b = b;
	}
	
	public String getA(){
		return a;
	}
	
	public String getB(){
		return b;
	}
	
	public void setA(String a){
		this.a = a;
	}
	
	public void setB(String b){
		this.b = b;
	}
	
}
	